    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
                    // TODO add your handling code here:
        String rollno=jTextField2.getText();
        String physics=jTextField3.getText();
        String chemistry=jTextField4.getText();
        String Maths=jTextField5.getText();
        String electricalMachines=jTextField6.getText();
        String dbms=jTextField1.getText();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/studenresultproject","root","honor6X@umair");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student where rollno='"+rollno+"'");
            if(rs.next()){
                st.executeUpdate("insert into result values('"+rollno+"','"+physics+"','"+chemistry+"','"+Maths+"','"+electricalMachines+"','"+dbms+"')");
                JOptionPane.showMessageDialog(null,"Successfully Added the result");
                setVisible(false);
                new insertNewResult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"Roll Number: '"+rollno+"' does not exist");
            }
            
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,String.valueOf(e));
        }
        
    }//GEN-LAST:event_jButton6ActionPerformed

